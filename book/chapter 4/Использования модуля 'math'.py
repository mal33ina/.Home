from math import sqrt, sin, cos, tan, pi, e
x = float(input('number x: '))
kvadrat = ('% .3f '%sqrt(x)), 'квадрат икса'
sinus = ('% .3f '%sin(x)), 'sin x'
cosin = cos(x) * sin(x), "cos * sin"
tanges = ('% .3f '%tan(x)), 'tanges x'
chislo_pi_x = x * pi, 'chislo pi * x'
e_x = x + e, 'e + x'
print(kvadrat)
print(sinus)
print(cosin)
print(tanges)
print(chislo_pi_x)
print(e_x)
